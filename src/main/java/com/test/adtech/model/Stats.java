package com.test.adtech.model;

public class Stats {
	
	private Long deliveries;
	private Long clicks;
	private Long installs;
	
	
	/**
	 * @return the deliveries
	 */
	public Long getDeliveries() {
		return deliveries;
	}
	/**
	 * @param deliveries the deliveries to set
	 */
	public void setDeliveries(Long deliveries) {
		this.deliveries = deliveries;
	}
	/**
	 * @return the clicks
	 */
	public Long getClicks() {
		return clicks;
	}
	/**
	 * @param clicks the clicks to set
	 */
	public void setClicks(Long clicks) {
		this.clicks = clicks;
	}
	/**
	 * @return the installs
	 */
	public Long getInstalls() {
		return installs;
	}
	/**
	 * @param installs the installs to set
	 */
	public void setInstalls(Long installs) {
		this.installs = installs;
	}
	

	
	

}
